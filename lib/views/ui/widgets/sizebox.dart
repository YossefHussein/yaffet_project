import 'package:flutter/material.dart';
import 'package:flutter_screenutil/flutter_screenutil.dart';

/// there optional parameter because. if any developer want change in ilmenite case
/// can change size to special size
/// this for sizebox for height
class PSizeboxHeight5 extends StatefulWidget {
  double? height = 5.0;
  PSizeboxHeight5({super.key, this.height = 5.0});

  @override
  State<PSizeboxHeight5> createState() => _PSizeboxHeight5State();
}

class _PSizeboxHeight5State extends State<PSizeboxHeight5> {
  @override
  Widget build(BuildContext context) {
    return SizedBox(height: widget.height?.sp);
  }
}

